package com.neil.beans;

public class SimpleBean {
	public SimpleBean(){
		System.out.println("-----an new object-----");
	}
	private String name;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	private int age;
}
